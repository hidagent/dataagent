def render_todo_list(todos: list[dict]) -> None:
    """Render todo list as a panel."""
    if not todos:
        return
    lines = []
    for todo in todos:
        status = todo.get("status", "pending")
        content = todo.get("content", "")
        if status == "completed":
            lines.append(f"[green]‚òë {content}[/green]")
        elif status == "in_progress":
            lines.append(f"[yellow]‚è≥ {content}[/yellow]")
        else:
            lines.append(f"[dim]‚òê {content}[/dim]")
    console.print(Panel("\n".join(lines), title="[bold]Task List[/bold]", border_style="cyan", box=box.ROUNDED))
